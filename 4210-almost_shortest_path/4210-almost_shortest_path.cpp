#include <stdio.h>
#include <stdlib.h>
#include <cstring>
#include <iostream>
#include <cstdint>
#include <algorithm>

using namespace std;

int main(void)
{
   while(1)
   {
      int N, M, S, D, U, V, P;
      scanf("%d %d", &N, &M);

      if(!N and !M) return 0;

      scanf("%d %d", &S, &D);

      for(int i=0; i<M; i++)
      {
         scanf("%d %d %d", &U, &V, &P);
      }
   }
}
